// LOOPS

// FOR

for(var inicial = 0; inicial <= 10; inicial++){
  console.log("for incremental: ", inicial)
}

for(var inicial = 10; inicial >= 0; inicial--){
  console.log("for decremento: ", inicial)
}

// While

var inicial = 0;
while(inicial < 10){
  console.log('while incremental: ', inicial)
  inicial++;
}

// DO WHILE
inicial = 0;
do{
  console.log('dowhile incrmeental: ', inicial)
  inicial++;
}while(inicial < 10)
